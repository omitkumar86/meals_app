import 'dart:ui';
import 'package:google_fonts/google_fonts.dart';

/// For Montserrat Google Font
myStyleJost(double fs, Color clr, [FontWeight ?fw, FontStyle? fontStyle]){
  return GoogleFonts.jost(fontSize: fs, color: clr, fontWeight: fw, fontStyle: fontStyle);
}


/// For Oxanium Google Font
myStyleOxanium(double fs, Color clr, [FontWeight ?fw, FontStyle? fontStyle]){
  return GoogleFonts.oxanium(fontSize: fs, color: clr, fontWeight: fw, fontStyle: fontStyle);
}